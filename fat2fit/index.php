                            function login() {
                                var email = $('#email').val();
                                var pwd = $('#pwd').val();
                                $.ajax({
                                    method: 'POST',
                                    url: 'loginAjax.php',
                                    data: {'email': email, 'pwd': pwd},
                                    dataType: 'json',
                                    success: function (data) {
                                        if (data.status === "errorEmail") {
                                            $('#errLogin').show();
                                            $('#errLogin').text('E-mail incorrect !');
                                        }
                                        if (data.status === "errorPwd") {
                                            $('#errLogin').show();
                                            $('#errLogin').text('Mot de passe incorrect !');
                                        }
                                        if (data.status === "errorUnverified") {
                                            $('#errLogin').show();
                                            $('#errLogin').text('E-mail pas encore v√©rifi√© !');
                                        }
                                        if (data.status === "success") {
                                            document.location.href = "userGoalsIndex.php";
                                        }
                                    },
                                    error: function (jqXHR) {
                                        $('#errLogin').text(jqXHR);
                                    }
                                });
                            }

                            $(document).ready(function () {
                                $('#errLogin').hide();
                            });
